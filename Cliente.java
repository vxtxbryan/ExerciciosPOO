package br.com.generation.poo;

public class Cliente {

	
	 private String NomeCliente;
	 private String IdadeCliente;
	 private String IdentidadeCliente;
	 private int ValorCompra;
	 
	 
	 
	 
	public String getNomeCliente() {
		return NomeCliente;
	}
	public void setNomeCliente(String nomeCliente) {
		NomeCliente = nomeCliente;
	}
	public String getIdadeCliente() {
		return IdadeCliente;
	}
	public void setIdadeCliente(String idadeCliente) {
		IdadeCliente = idadeCliente;
	}
	public String getIdentidadeCliente() {
		return IdentidadeCliente;
	}
	public void setIdentidadeCliente(String identidadeCliente) {
		IdentidadeCliente = identidadeCliente;
	}
	public int getValor() {
		return ValorCompra;
	}
	public void setValor(int valor) {
		ValorCompra = valor;
	}
	

	  
	  
	  
	  
	  
	  
	  
	  
}

